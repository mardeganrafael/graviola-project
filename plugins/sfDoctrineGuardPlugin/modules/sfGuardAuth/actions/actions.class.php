class sfGuardAuthActions extends BasesfGuardAuthActions
{
  /** Override BasesfGuardAuthActions to check redirect path */
  public function  executeSignin($request) {
    
    $user = $this->getUser();
    if ($user->isAuthenticated())
    {
      return $this->redirect('@homepage');
    }

    $class = sfConfig::get('app_sf_guard_plugin_signin_form', 'sfGuardFormSignin');
    $this->form = new $class();

    if ($request->isMethod('post'))
    {
      $this->form->bind($request->getParameter('signin'));
      if ($this->form->isValid())
      {
        $values = $this->form->getValues();
        $this->getUser()->signin($values['user'], array_key_exists('remember', $values) ? $values['remember'] : false);

        //Load all groups check against user logged and redirect accordingly

        $groupAssociated = $this->getUser()->getGroupNames();
        if (!empty ($groupAssociated))
        {
          $routingGroup = NjLoginGroupRoutingTable::getInstance()->getRoutingByGroup($groupAssociated[0]);
          if (isset($routingGroup[0]['routing'])) {
            $signinUrl = $routingGroup[0]['routing'];
          }
          else {
            $signinUrl = '@homepage';
          }
        }
        return $this->redirect($signinUrl);
      }
    }
    else
    {
      if ($request->isXmlHttpRequest())
      {
        $this->getResponse()->setHeaderOnly(true);
        $this->getResponse()->setStatusCode(401);

        return sfView::NONE;
      }

      // if we have been forwarded, then the referer is the current URL
      // if not, this is the referer of the current request
      $user->setReferer($this->getContext()->getActionStack()->getSize() > 1 ? $request->getUri() : $request->getReferer());

      $module = sfConfig::get('sf_login_module');
      if ($this->getModuleName() != $module)
      {
        return $this->redirect($module.'/'.sfConfig::get('sf_login_action'));
      }

      $this->getResponse()->setStatusCode(401);
    }
  }

  public function  executePassword($request) {
    parent::executePassword($request);
  }

  public function  executeSecure($request) {
    parent::executeSecure($request);
  }

  public function  executeSignout($request) {
    parent::executeSignout($request);
  }
}
